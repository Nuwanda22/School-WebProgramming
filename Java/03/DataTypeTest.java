
public class DataTypeTest {
	public static void main(String[] args)
    {
		System.out.println("16진수값 :" + 0x11);
		System.out.println("8진수값 :" + 011);
		System.out.println("십진수값 : " + 111);
		System.out.println("2.0f / 3.0f = " + 2.0f / 3.0f);
		System.out.println("2.0 / 3.0 = " + 2.0 / 3.0);
		System.out.println("2/3 = " + 2 / 3);
		System.out.println("지수형 데이터 3.14159e-3 =" + 3.14159e-3);
		System.out.println("이진형 데이터 값 :" + true + (10 > 20));
		System.out.println("특수문자값 : \\ \"");
		System.out.println("유니코드값 : \u004a \u004f");
    }
}