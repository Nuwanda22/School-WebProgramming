
class Calculater {
	public String plus(int a, int b){
		return String.valueOf(a + b);
	}
	public String minus(int a, int b){
		return String.valueOf(a - b);
	}
	public String multi(int a, int b){
		return String.valueOf(a * b);
	}
	public String div(int a, int b){
		return String.valueOf((double)a / b);
	}
}
